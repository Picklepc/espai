@router.post("/import-zip")
async def import_project_from_zip(
    file: UploadFile = File(...),
    name: str = Form(""),
    description: str = Form(""),
):
    """
    Create a new ESPAI project from an uploaded ZIP file.

    Accepts PlatformIO, Arduino IDE, or any ESP32 project zip.  Extracts the
    source to projects/{id}/source/, scaffolds the full ESPAI project structure
    alongside it, and auto-creates a draft 'port-to-hub' Agent Bench task so
    Claude can analyse and port the project on demand.
    """
    if not (file.filename or "").lower().endswith(".zip"):
        raise HTTPException(400, "Only .zip files are accepted")

    content = await file.read(_ZIP_MAX_BYTES + 1)
    if len(content) > _ZIP_MAX_BYTES:
        raise HTTPException(413, "ZIP must be under 100 MB")

    try:
        zf = zipfile.ZipFile(io.BytesIO(content))
    except zipfile.BadZipFile:
        raise HTTPException(400, "File is not a valid ZIP archive")

    # Derive project name from zip filename when not supplied
    zip_stem = Path(file.filename or "imported").stem
    project_name = name.strip() or zip_stem or "Imported Project"
    desc = description.strip() or None

    # Create project in DB and scaffold directories
    project_id = secrets.token_hex(6)
    slug = _to_hostname(project_name)
    now = _now()
    with get_conn() as conn:
        conn.execute(
            "INSERT INTO projects (id, name, description, devices, slug, created) VALUES (?,?,?,?,?,?)",
            (project_id, project_name, desc or "", json.dumps([]), slug, now),
        )
    _create_project_folder(project_id, project_name, desc)

    # Extract ZIP → projects/{id}/source/
    proj_dir  = PROJECTS_DIR / project_id
    source_dir = proj_dir / "source"
    source_dir.mkdir(exist_ok=True)

    # Persist the original ZIP for reference
    (proj_dir / "source.zip").write_bytes(content)

    # Detect whether the ZIP has a single top-level folder to strip
    names = zf.namelist()
    top_prefix = ""
    if names:
        first = names[0].split("/")[0]
        if first and all(n == first or n.startswith(first + "/") for n in names):
            top_prefix = first + "/"

    file_count   = 0
    has_pio      = False
    has_arduino  = False
    source_root  = str(source_dir.resolve())

    for member in zf.infolist():
        rel = member.filename
        if top_prefix:
            rel = rel[len(top_prefix):]
        if not rel:
            continue
        # Drop build artifacts / caches
        if any(rel.startswith(p) or ("/" + p) in rel for p in _ZIP_SKIP_PREFIXES):
            continue
        # Guard against path traversal
        dest = (source_dir / rel).resolve()
        if not str(dest).startswith(source_root):
            continue
        if member.is_dir():
            dest.mkdir(parents=True, exist_ok=True)
        else:
            dest.parent.mkdir(parents=True, exist_ok=True)
            dest.write_bytes(zf.read(member.filename))
            file_count += 1
            if rel in ("platformio.ini",) or rel.endswith("/platformio.ini"):
                has_pio = True
            if rel.endswith(".ino"):
                has_arduino = True

    zf.close()

    project_type = "platformio" if has_pio else "arduino" if has_arduino else "unknown"

    # Register mDNS advertisement
    _mdns.register_project(slug, project_id)

    # Write ESPAI.md with ported-project integration patterns
    proj_dir = PROJECTS_DIR / project_id
    proj_dir.mkdir(parents=True, exist_ok=True)
    espai_md = _generate_espai_md(
        project_id, project_name, description or "",
        device_type="esp32", ported=True,
    )
    (proj_dir / "ESPAI.md").write_text(espai_md, encoding="utf-8")

    # Auto-create draft porting task (best-effort — Agent Bench may be disabled)
    task_id = None
    try:
        task_id = str(uuid.uuid4())
        allowed = [f"projects/{project_id}/", "workers/", "recipes/", "cards/"]
        task_desc = _build_port_task_description(
            file.filename or "upload.zip",
            project_id,
            project_name,
            project_type,
        )
        with get_conn() as conn:
            conn.execute(
                """INSERT INTO agent_tasks
                   (id, project_id, title, description, template, status,
                    allowed_paths, acceptance_criteria, context,
                    context_type, context_id, parent_task_id,
                    lane, adapter_id, created, updated)
                   VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)""",
                (
                    task_id, project_id,
                    f"Port '{project_name}' to ESPAI",
                    task_desc,
                    "port-to-hub", "draft",
                    json.dumps(allowed),
                    json.dumps(_PORT_CRITERIA),
                    None,
                    "project", project_id, None,
                    "dev", None,
                    now, now,
                ),
            )
    except Exception:
        task_id = None  # agent bench disabled or DB error — not fatal

    return {
        "id":            project_id,
        "name":          project_name,
        "slug":          slug,
        "created":       now,
        "file_count":    file_count,
        "has_platformio": has_pio,
        "has_arduino":   has_arduino,
        "project_type":  project_type,
        "task_id":       task_id,
    }


# ── Matter config ──────────────────────────────────────────────────────────────

_MATTER_DEFAULTS = {
    "matter_enabled":         False,
    "matter_device_type":     "on_off_plug",
    "matter_label":           "",
    "matter_state_map":       {},
    "matter_command_actions": {},
    "matter_endpoint_id":     None,
}
_MATTER_KEYS = set(_MATTER_DEFAULTS)


@router.get("/{project_id}/matter")
def get_project_matter(project_id: str):
    """Read per-project Matter configuration from .ESPAI-project.json."""
    cfg_file = PROJECTS_DIR / project_id / ".ESPAI-project.json"
    if not cfg_file.exists():
        raise HTTPException(404, "Project not found")
    try:
        full = json.loads(cfg_file.read_text(encoding="utf-8"))
    except Exception:
        full = {}
    result = {**_MATTER_DEFAULTS}
    for k in _MATTER_KEYS:
        if k in full:
            result[k] = full[k]
    return result


_MATTER_DEVICE_TYPES = {
    "on_off_plug", "dimmable_light", "color_light",
    "temperature_sensor", "humidity_sensor", "occupancy_sensor", "contact_sensor",
}


@router.put("/{project_id}/matter")
def set_project_matter(project_id: str, body: dict):
    """Write per-project Matter configuration; sync bridge on enable/disable."""
    from .. import matter_bridge as _mb

    cfg_file = PROJECTS_DIR / project_id / ".ESPAI-project.json"
    if not cfg_file.exists():
        raise HTTPException(404, "Project not found")

    dtype = body.get("matter_device_type")
    if dtype is not None and dtype not in _MATTER_DEVICE_TYPES:
        raise HTTPException(
            400,
            f"Unknown matter_device_type {dtype!r}. "
            f"Valid types: {sorted(_MATTER_DEVICE_TYPES)}",
        )

    try:
        full = json.loads(cfg_file.read_text(encoding="utf-8"))
    except Exception:
        full = {}

    was_enabled = bool(full.get("matter_enabled", False))
    for k in _MATTER_KEYS:
        if k in body and k != "matter_endpoint_id":   # endpoint_id assigned by bridge
            full[k] = body[k]

    cfg_file.write_text(json.dumps(full, indent=2), encoding="utf-8")

    now_enabled = bool(full.get("matter_enabled", False))
    if _mb.is_running() and (was_enabled != now_enabled or now_enabled):
        _mb.sync_project(project_id)

    return {k: full.get(k, _MATTER_DEFAULTS[k]) for k in _MATTER_KEYS}
